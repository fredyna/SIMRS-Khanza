    private void buttonBig6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBig6ActionPerformed
        // TODO add your handling code here:
        try {
            ps = connect.prepareStatement("delete from ttd_dokter where kd_dokter=?");
            try {
                ps.setString(1, dokter);
                ps.executeUpdate();

                file = new File("./setting/" + dokter + ".png");
                
                Files.delete(file.getAbsoluteFile().toPath());
                JOptionPane.showMessageDialog(null, "Proses Hapus TTD berhasil.");
                canvas1.clear();
            } catch (Exception e) {
                System.out.println("Notifikasi Hapus : " + e);
                JOptionPane.showMessageDialog(null, "Maaf, data gagal dihapus. Kemungkinan data tersebut masih dipakai di table lain...!!!!");
            } finally {
                if (ps != null) {
                    ps.close();
                }
            }
        } catch (Exception e) {
            System.out.println("Notifikasi Hapus : " + e);
        }
        canvas1.setThickness(5);
        buttonBig4.setEnabled(true);
    }//GEN-LAST:event_buttonBig6ActionPerformed

