public class Bangsal {
    private String kd_bangsal;
    private String nama_bangsal;
    private String status;

    /**
     *
     * @return
     */
    public String getKd_bangsal() {
        return kd_bangsal;
    }

    /**
     *
     * @param kd_bangsal
     */
    public void setKd_bangsal(String kd_bangsal) {
        this.kd_bangsal = kd_bangsal;
    }

    /**
     *
     * @return
     */
    public String getNama_bangsal() {
        return nama_bangsal;
    }

    /**
     *
     * @param nama_bangsal
     */
    public void setNama_bangsal(String nama_bangsal) {
        this.nama_bangsal = nama_bangsal;
    }

    /**
     *
     * @return
     */
    public String getStatus() {
        return status;
    }

    /**
     *
     * @param status
     */
    public void setStatus(String status) {
        this.status = status;
    }

}